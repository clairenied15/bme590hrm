import numpy as np
from num_beats_bme590hrm import n_beats


def test_num_beats():
    s = n_beats(list(np.arange(0, 5, 0.1)), np.array([-0.5, -0.3, 0,
                                                      0.6, 1.9,
                                                      4, 2.7, 1.7,
                                                      1.2, 1,
                                                      -0.5, -0.3, 0,
                                                      0.7, 1.7,
                                                      4, 2.5, 1.7, 1.2,
                                                      1, -0.7,
                                                      -0.4, 0, 0.8, 1.8,
                                                      4, 2.5,
                                                      1.7, 1.2, 1, -0.5,
                                                      -0.3, 0,
                                                      0.7, 1.7, 4,
                                                      2.5, 1.7,
                                                      1.2, 1,
                                                      -0.5, -0.3, 0,
                                                      0.7, 1.7, 4,
                                                      2.5, 1.7, 1.2, 1]))
    assert s[2] == 5
